vim.cmd("nmap <leader>w :w<cr>")
vim.cmd("nmap <leader>Q :qa!<cr>")
vim.keymap.set("n", "<leader>q", ":bd<CR>", { desc = "Close buffer" })
vim.keymap.set("v", ">", ">gv")
vim.keymap.set("v", "<", "<gv")
vim.keymap.set("n", "<leader>j", ":bprevious<CR>", { desc = "Previous buffer" })
vim.keymap.set("n", "<leader>k", ":bnext<CR>", { desc = "Next buffer" })
vim.keymap.set("n", "<leader>p", "<C-^>", { desc = "Switch to last buffer" })
vim.keymap.set("n", "<leader>a", ":%bd<CR>", { desc = "Close all buffers" })
vim.keymap.set("n", "<leader>v", function()
    vim.cmd("vsplit")
    vim.cmd("wincmd l")
end)
vim.keymap.set("n", "<leader>h", "<C-w>h")
vim.keymap.set("n", "<leader>l", "<C-w>l")
vim.keymap.set("n", "gd", vim.lsp.buf.definition, { silent = true })
vim.keymap.set("n", "<leader>rs", vim.lsp.buf.rename, { silent = true })
vim.keymap.set("n", "<leader>ca", vim.lsp.buf.code_action, { silent = true })
local function open_float_focus()
    vim.diagnostic.open_float(nil, { scope = "line" })
    vim.diagnostic.open_float(nil, { scope = "line", focus = true })
end
vim.keymap.set("n", "df", open_float_focus, { silent = true })
vim.keymap.set("n", "<M-j>", ":vertical resize +10<CR>")
vim.keymap.set("n", "<M-k>", ":vertical resize -10<CR>")
vim.keymap.set("n", "<leader>cr", function()
    local path = vim.fn.expand("%")
    vim.fn.setreg("+", path)
    print("Copied relative path: " .. path)
end, { desc = "Copy relative file path" })
vim.keymap.set("n", "<leader>cp", function()
    local path = vim.fn.expand("%:p")
    vim.fn.setreg("+", path)
    print("Copied absolute path: " .. path)
end, { desc = "Copy absolute file path" })
vim.keymap.set("n", "<leader>y", function()
    vim.cmd("normal! mzggyG`z")
    print("buffer yanked")
end, {
    desc = "Yank entire buffer, keep cursor",
})
